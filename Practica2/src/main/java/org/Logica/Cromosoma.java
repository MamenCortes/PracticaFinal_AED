/**
 * Los tipos de cromosoma que puede tener un ratón
 * @author Mamen Cortés Navarro
 */
public enum Cromosoma {
    /**
     * Cromosoma x normal
     */
    X,
    /**
     * Cromosoma X mutado
     */
    Xmut,
    /**
     * Cromosoma Y normal
     */
    Y,
    /**
     * Cromosoma Y mutado
     */
    Ymut
}
